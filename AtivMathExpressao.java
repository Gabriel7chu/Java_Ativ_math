public class AtivMathExpressao {
    public static void main(String[] args) {
        // 1. Calcule (10 + 5) * 2
        int resultado1 = (10 + 5) * 2;
        System.out.println("(10 + 5) * 2 = " + resultado1);

        // 2. Determine o valor de (20 - 4) / 2
        int resultado2 = (20 - 4) / 2;
        System.out.println("(20 - 4) / 2 = " + resultado2);

        // 3. Resolva 15 + 3 * 2
        int resultado3 = 15 + 3 * 2;
        System.out.println("15 + 3 * 2 = " + resultado3);

        // 4. Calcule 100 / (2 + 3)
        int resultado4 = 100 / (2 + 3);
        System.out.println("100 / (2 + 3) = " + resultado4);

        // 5. Determine o valor de 25 % 4
        int resultado5 = 25 % 4;
        System.out.println("25 % 4 = " + resultado5);

        // 6. Resolva (30 - 5) * (2 + 3)
        int resultado6 = (30 - 5) * (2 + 3);
        System.out.println("(30 - 5) * (2 + 3) = " + resultado6);

        // 7. Calcule (50 / 2) + (8 * 3)
        int resultado7 = (50 / 2) + (8 * 3);
        System.out.println("(50 / 2) + (8 * 3) = " + resultado7);

        // 8. Resolva 2 * (3 + 4) - 5
        int resultado8 = 2 * (3 + 4) - 5;
        System.out.println("2 * (3 + 4) - 5 = " + resultado8);

        // 9. Determine o valor de 100 - (25 / 5)
        int resultado9 = 100 - (25 / 5);
        System.out.println("100 - (25 / 5) = " + resultado9);

        // 10. Calcule (8 + 2) * (6 - 4)
        int resultado10 = (8 + 2) * (6 - 4);
        System.out.println("(8 + 2) * (6 - 4) = " + resultado10);

        // 11. Resolva 45 % (7 + 3)
        int resultado11 = 45 % (7 + 3);
        System.out.println("45 % (7 + 3) = " + resultado11);

        // 12. Calcule (12 / 3) + (18 / 6)
        int resultado12 = (12 / 3) + (18 / 6);
        System.out.println("(12 / 3) + (18 / 6) = " + resultado12);

        // 13. Determine o valor de 2^3 + 5 * 2
        int resultado13 = (int) Math.pow(2, 3) + 5 * 2;
        System.out.println("2^3 + 5 * 2 = " + resultado13);

        // 14. Resolva (15 - 5) * (3^2)
        int resultado14 = (15 - 5) * (int) Math.pow(3, 2);
        System.out.println("(15 - 5) * (3^2) = " + resultado14);

        // 15. Calcule (8 % 3) + (6 / 2)
        int resultado15 = (8 % 3) + (6 / 2);
        System.out.println("(8 % 3) + (6 / 2) = " + resultado15);

        // 16. Resolva (7 + 5) - (3 * 2)
        int resultado16 = (7 + 5) - (3 * 2);
        System.out.println("(7 + 5) - (3 * 2) = " + resultado16);

        // 17. Determine o valor de (9 * 9) - (81 / 9)
        int resultado17 = (9 * 9) - (81 / 9);
        System.out.println("(9 * 9) - (81 / 9) = " + resultado17);

        // 18. Calcule (50 / (5 + 5)) + 10
        int resultado18 = (50 / (5 + 5)) + 10;
        System.out.println("(50 / (5 + 5)) + 10 = " + resultado18);

        // 19. Resolva (16 % 5) * (7 - 3)
        int resultado19 = (16 % 5) * (7 - 3);
        System.out.println("(16 % 5) * (7 - 3) = " + resultado19);

        // 20. Determine o valor de (10 + 5) / (2^2)
        int resultado20 = (10 + 5) / (int) Math.pow(2, 2);
        System.out.println("(10 + 5) / (2^2) = " + resultado20);
    }
}